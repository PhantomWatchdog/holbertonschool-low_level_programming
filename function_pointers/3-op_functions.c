#include "3-calc.h"

/**
 *op_add - Addition.
 *@a: Num1.
 *@b: Num2.
 *Return:result.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - Substraction.
 *@a: Num1.
 *@b: Num2.
 *Return:result.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - Multiplication.
 *@a: Num1.
 *@b: Num2.
 *Return:result.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - Division.
 *@a: Num1.
 *@b: Num2.
 *Return:result.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - Remainder.
 *@a: Num1.
 *@b: Num2.
 *Return:result.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
